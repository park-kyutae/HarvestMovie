package kr.event.vo;

public class EventFavVO {
	private int event_fav_num;
	private int event_board_num;
	private int mem_num;
	public int getEvent_fav_num() {
		return event_fav_num;
	}
	public void setEvent_fav_num(int event_fav_num) {
		this.event_fav_num = event_fav_num;
	}
	public int getEvent_board_num() {
		return event_board_num;
	}
	public void setEvent_board_num(int event_board_num) {
		this.event_board_num = event_board_num;
	}
	public int getMem_num() {
		return mem_num;
	}
	public void setMem_num(int mem_num) {
		this.mem_num = mem_num;
	}
	
}
