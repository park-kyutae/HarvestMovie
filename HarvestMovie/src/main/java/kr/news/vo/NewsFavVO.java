package kr.news.vo;

public class NewsFavVO {
	
	private int news_fav_num;
	private int news_num;
	private int mem_num;
	
	public int getNews_fav_num() {
		return news_fav_num;
	}
	public void setNews_fav_num(int news_fav_num) {
		this.news_fav_num = news_fav_num;
	}
	public int getNews_num() {
		return news_num;
	}
	public void setNews_num(int news_num) {
		this.news_num = news_num;
	}
	public int getMem_num() {
		return mem_num;
	}
	public void setMem_num(int mem_num) {
		this.mem_num = mem_num;
	}
	
	
}
